void main() {
  // 1. for loop
  // Using a for loop to print numbers from 1 to 5
  print('--- For Loop ---');
  for (int i = 1; i <= 5; i++) {
    print('For loop iteration: $i');
  }

  // 2. while loop
  // Using a while loop to print numbers from 1 to 5
  print('--- While Loop ---');
  int j = 1;
  while (j <= 5) {
    print('While loop iteration: $j');
    j++;
  }
}
